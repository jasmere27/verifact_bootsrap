    <script>
        const input = document.getElementById('passwordInput');
        const result = document.getElementById('passwordStrength');

        input.addEventListener('input', function () {
            const val = input.value;
            let strength = 'Weak';
            let className = 'weak';

            const lengthCriteria = val.length >= 12;
            const upper = /[A-Z]/.test(val);
            const lower = /[a-z]/.test(val);
            const number = /[0-9]/.test(val);
            const symbol = /[!@#$%^&*(),.?":{}|<>]/.test(val);

            const passedCriteria = [upper, lower, number, symbol].filter(Boolean).length;

            if (lengthCriteria && passedCriteria >= 3) {
                strength = 'Strong';
                className = 'strong';
            } else if (val.length >= 8 && passedCriteria >= 2) {
                strength = 'Medium';
                className = 'medium';
            }

            result.textContent = `Password Strength: ${strength}`;
            result.className = `strength ${className}`;
        });
    </script>



